#include "printf.h"

int	printf_u(unsigned int i, int *n)
{
	char			*decimal;

	decimal = "0123456789";
	if (i >= 10)
		printf_di(i / 10, n);
	n += write(1, decimal[i % 10], 1);
	return (*n);
}
 