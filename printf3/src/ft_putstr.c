#include "ft_printf.h"

int	ft_putstr(char *s, int *n)
{
	if (!s)
	{
		write(1, "(null)", 6);
		*n += 6;
	}
	else
	{
		while (*s)
		{
			*n += ft_putchar2(*s);
			s++;
		}
	}
	return (*n);
}
